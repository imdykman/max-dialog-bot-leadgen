// ========== ОБРАБОТЧИК ДЕЙСТВИЙ ПОДДЕРЖКИ ==========
async function handleSupportAction(ctx, userId, data, userStates) {
  if (data === 'support') return handleSupport(ctx, userId, userStates);
  if (data === 'support_ask') return handleSupportAsk(ctx, userId);
  
  // Обработка вопросов из FAQ
  if (data.startsWith('support_') && FAQ[data]) {
    return handleFaqAnswer(ctx, userId, data);
  }
}

