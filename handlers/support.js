// ========== ГЛАВНОЕ МЕНЮ ПОДДЕРЖКИ ==========
async function handleSupport(ctx, userId, userStates) {
  userStates.set(userId, { mode: 'support' });
  
  await ctx.reply(
    `🆘 *Поддержка*\n\n` +
    `━━━━━━━━━━━━━━━━━━━━━━\n\n` +
    `Здравствуйте! Я помогу разобраться с любыми вопросами о разработке ботов.\n\n` +
    `Выберите интересующий вопрос или задайте свой:`,
    {
      attachments: [Keyboard.inlineKeyboard([
        [Keyboard.button.callback('💰 Сколько стоит бот?', 'support_price')],
        [Keyboard.button.callback('⏱ Сроки разработки', 'support_time')],
        [Keyboard.button.callback('🔌 Интеграции', 'support_integrations')],
        [Keyboard.button.callback('🛠 Обслуживание', 'support_maintenance')],
        [Keyboard.button.callback('🚀 Как начать?', 'support_start')],
        [Keyboard.button.callback('📱 Платформы', 'support_platforms')],
        [Keyboard.button.callback('🔒 Безопасность', 'support_security')],
        [Keyboard.button.callback('📲 Приложение в MAX', 'support_apps')],
        [Keyboard.button.callback('💬 Задать свой вопрос', 'support_ask')],
        [Keyboard.button.callback('🏠 Главное меню', 'start')]
      ])]
    }
  );
  
  if (userId) trackEvent('support_open', userId);
}

// ========== ПОКАЗ ОТВЕТА НА ВОПРОС ==========
async function handleFaqAnswer(ctx, userId, questionKey) {
  const faq = FAQ[questionKey];
  if (!faq) return;
  
  await ctx.reply(faq.answer, {
    attachments: [Keyboard.inlineKeyboard([
      [Keyboard.button.callback('❓ Другой вопрос', 'support')],
      [Keyboard.button.callback('💼 Заказать бота', 'order_start')],
      [Keyboard.button.callback('🏠 Главное меню', 'start')]
    ])]
  });
  
  trackEvent('faq_view', userId, { question: questionKey });
}

